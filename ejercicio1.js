let numero = prompt("Ingrese un Número") //solicitamos un numero al usuario
    //definimos un condicional para saber si el numero es par o impar
if (numero % 2  == 0 ){ //si el numero tiene como resultado 0 al aplicarle módulo 2 el resultado será par
    alert("el " + numero + " es par") 
}
else { //si el resultado no es 0, será impar
    alert("el " + numero + " es impar")
}

